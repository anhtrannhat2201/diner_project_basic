// dark theme
document.getElementById('switchTheme').onclick = function () {
    document.getElementById("group12").classList.toggle("dark");
};

// open overlay menu
document.getElementById('openMenu').onclick = function () {
    document.getElementById("group12").classList.toggle("open");
};

// close overlay menu
document.getElementById('closeOverlay').onclick = function () {
    document.getElementById("group12").classList.toggle("open");
};


 